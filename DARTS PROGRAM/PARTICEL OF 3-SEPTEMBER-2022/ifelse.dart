void main() {
  int marks = 36;

  if (marks >= 70 && marks <= 100) {
    print("A garde");
  } else if (marks >= 60 && marks <= 70) {
    print("B garde");
  } else if (marks >= 50 && marks <= 60) {
    print("C garde");
  } else if (marks >= 40 && marks <= 50) {
    print("D grade");
  } else {
    print("Fail");
  }
}
